class Calculator 
  def initialize()
  end
  def self.sum(number1, number2)
    @result = number1 + number2
  end

  def self.sub(number1, number2)
    @result = number1 - number2
  end

  def self.mul(number1, number2)
    @result = number1 * number2
  end

  def self.div(number1, number2)
    @result = number1 / number2
  end
end